/**
 * Get all keywords as a flat array (for meta keywords tag)
 * @returns {string[]} All keywords from all languages
 */
export function getAllKeywords() {
  return Object.values(seoKeywords).flat();
}

/**
 * Get keywords for specific languages
 * @param {string[]} langs - Array of language codes (e.g., ['en', 'ru', 'de'])
 * @returns {string[]} Keywords for specified languages
 */
export function getKeywordsForLanguages(langs) {
  return langs
    .filter((lang) => seoKeywords[lang])
    .flatMap((lang) => seoKeywords[lang]);
}

/**
 * Get primary keywords (limited set for meta tag)
 * Takes first N keywords from each language
 * @param {number} perLang - Number of keywords per language
 * @returns {string[]} Primary keywords
 */
export function getPrimaryKeywords(perLang = 5) {
  return Object.values(seoKeywords).flatMap((keywords) =>
    keywords.slice(0, perLang)
  );
}

export default seoKeywords;
